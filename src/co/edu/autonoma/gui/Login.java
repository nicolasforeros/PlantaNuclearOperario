    private void BtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEntrarActionPerformed
        
        String nombreOperario = TxtFNombreOperario.getText();
        
        this.operario.setNombre(nombreOperario);
        
        this.operario.setIpServidor(txtfIpServidor.getText());
        
        if(this.operario.getNombre().trim().isEmpty() | this.operario.getIpServidor().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Completar los datos del login", "Login", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        boolean conexion = this.operario.conectarAServidor();
        
        if(conexion){
            PanelControlPlantaN principal = new PanelControlPlantaN(operario);
            
            principal.setLocationRelativeTo(null);
            principal.setVisible(true);
            principal.iniciarEntrada();
            
            this.dispose();
            
            
        }else{
            JOptionPane.showMessageDialog(this, "Error conectando con el servidor solicitado", "Conexi√≥n", JOptionPane.INFORMATION_MESSAGE);
        }    
    }//GEN-LAST:event_BtnEntrarActionPerformed

